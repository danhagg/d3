/*

Primitive type
True, False, Null, Undefined, String, Number

Primitive tyoes have literal representation of their values (that arent stored in a variable)

A variable holding a primitive directly contains the primitive value

Despite the fact that they have methods, primitive values themselves are not objects. JavaScript makes them look like objects to provide a consistent experience in the language

*/

console.log(typeof "Nicholas");
console.log(typeof undefined);
console.log(typeof null);

console.log(undefined == null); console.log(undefined === null);
